-- ---------------------------------------------------------------------------
-- シード用ユーザー作成
-- ---------------------------------------------------------------------------
create or replace function public.seed_auth_user(
  user_id uuid,
  display_name text
)
returns void
language plpgsql
security definer
set search_path = public, auth, extensions
as $$
declare
  seed_email text := format('seed+%s@swipe-vs.local', replace(user_id::text, '-', ''));
  seed_password text := extensions.crypt(
    seed_email,
    extensions.gen_salt('bf'::text)
  );
begin
  if not exists (select 1 from auth.users where id = user_id) then
    insert into auth.users (
      instance_id,
      id,
      aud,
      role,
      email,
      encrypted_password,
      email_confirmed_at,
      raw_app_meta_data,
      raw_user_meta_data,
      created_at,
      updated_at,
      confirmation_token,
      email_change,
      email_change_token_new,
      recovery_token
    ) values (
      '00000000-0000-0000-0000-000000000000',
      user_id,
      'authenticated',
      'authenticated',
      seed_email,
      seed_password,
      now(),
      '{"provider":"email","providers":["email"]}'::jsonb,
      jsonb_build_object('display_name', display_name),
      now(),
      now(),
      '',
      '',
      '',
      ''
    );

    insert into auth.identities (
      id,
      user_id,
      provider_id,
      identity_data,
      provider,
      last_sign_in_at,
      created_at,
      updated_at
    ) values (
      gen_random_uuid(),
      user_id,
      user_id::text,
      jsonb_build_object('sub', user_id::text, 'email', seed_email),
      'email',
      now(),
      now(),
      now()
    );
  end if;

  insert into public.profiles (id, display_name)
  values (user_id, display_name)
  on conflict (id) do update
    set display_name = excluded.display_name;
end;
$$;

